package JavaDemo;

public class SwitchCase {

	public static void main(String[] args) {

		String Name="Raju";
		
		switch (Name) {
		case "Vinod":
			System.out.println("My Name Is Vinod");
			break;
		case "Raju":
			System.out.println("My Name Is Raju");
		case "Ruturaj":
			System.out.println("My Name is ruturaj");
		default:
			System.out.println("My Name is Blank");
			break;
		}
		
	}

}
