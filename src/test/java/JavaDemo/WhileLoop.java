package JavaDemo;

public class WhileLoop {

	public static void main(String[] args) {

		int i=2;
		
		while(i<=15) {
			System.out.println(i);
			i++;
		}
		
		int j=10;
		
		while(j>=1) {
			System.out.println(j);
			j--;
		}
		
		for (int k=12;k<=30;k++) {
			
			System.out.println(k);
		}
	}

}
